# ==========================================================
# 0. 필요한 라이브러리 불러오기
# ==========================================================

# OpenCV 라이브러리
# 이미지 읽기, 색상 변환, 도형 그리기 등의 기능을 제공
import cv2

# NumPy 라이브러리
# 이미지의 픽셀값 계산과 평균값 계산 등에 사용
import numpy as np


# ==========================================================
# 1. 이미지 읽기
# ==========================================================

# 미로 이미지를 불러온다.
img = cv2.imread("C:/Users/user/Desktop/maze.png")

# 이미지 불러오기 실패시 프로그램 종료
if img is None:
    print("이미지를 읽을 수 없습니다.")
    exit()


# ==========================================================
# 2. 그레이스케일(흑백) 변환
# ==========================================================

# 컬러 이미지를 흑백 이미지로 변환한다.
# 흑백 이미지는 외곽선 검출이나 이진화에 사용하기 쉽다.
gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)


# ==========================================================
# 3. 이미지 이진화
# ==========================================================

# threshold()
# 픽셀값이 240보다 크면 0
# 240보다 작으면 255가 된다.
#
# THRESH_BINARY_INV를 사용했기 때문에
# 흰색은 검정으로,
# 검정은 흰색으로 반전된다.
#
# 결과적으로
#   여백 -> 검정
#   미로 -> 흰색
#
# 이 과정을 통해 미로만 쉽게 찾을 수 있다.
_, binary = cv2.threshold(
    gray,
    240,
    255,
    cv2.THRESH_BINARY_INV
)


# ==========================================================
# 4. 가장 큰 외곽선 찾기
# ==========================================================

# findContours()
#
# 이미지에서 외곽선을 찾아준다.
#
# RETR_EXTERNAL
# → 가장 바깥쪽 외곽선만 찾는다.
#
# CHAIN_APPROX_SIMPLE
# → 불필요한 점을 줄여 메모리를 절약한다.
contours, _ = cv2.findContours(
    binary,
    cv2.RETR_EXTERNAL,
    cv2.CHAIN_APPROX_SIMPLE
)

# 외곽선 중에서 가장 넓은 영역을 선택한다.
# 대부분의 경우 가장 큰 영역이 미로가 된다.
largest = max(contours, key=cv2.contourArea)


# ==========================================================
# 5. 미로 영역만 잘라내기
# ==========================================================

# boundingRect()
#
# 가장 큰 외곽선을 포함하는
# 최소 크기의 직사각형을 구한다.
#
# x, y : 시작 좌표
# w : 너비
# h : 높이
x, y, w, h = cv2.boundingRect(largest)

# 원본 이미지에서
# 미로 부분만 잘라낸다.
#
# 여백은 자동으로 제거된다.
maze_img = img[y:y+h, x:x+w]


# ==========================================================
# 6. 미로 정보 설정
# ==========================================================

# 미로는 9행 9열이다.
ROWS = 9
COLS = 9

# 잘라낸 미로 이미지의 높이와 너비를 구한다.
height, width = maze_img.shape[:2]

# 한 칸의 높이와 너비를 계산한다.
cell_h = height // ROWS
cell_w = width // COLS


# ==========================================================
# 7. 미로 배열 생성 준비
# ==========================================================

# 최종적으로
#
# 0 → 이동 가능
# 1 → 벽
#
# 형태의 2차원 리스트를 만들 것이다.
maze = []

# 시작점과 도착점을 저장할 변수
# 아직 찾지 않았으므로 None으로 초기화한다.
start = None
goal = None

# ==========================================================
# 8. 미로를 한 칸씩 분석하여 배열 생성
# ==========================================================

# 미로의 모든 행을 반복한다.
# r은 현재 행 번호(0~8)를 의미한다.
for r in range(ROWS):

    # 현재 행의 정보를 저장할 리스트
    row = []

    # 미로의 모든 열을 반복한다.
    # c는 현재 열 번호(0~8)를 의미한다.
    for c in range(COLS):

        # ---------------------------------------
        # 현재 칸의 좌표 계산
        # ---------------------------------------

        # 현재 칸의 위쪽 좌표
        y1 = r * cell_h

        # 현재 칸의 아래쪽 좌표
        y2 = (r + 1) * cell_h

        # 현재 칸의 왼쪽 좌표
        x1 = c * cell_w

        # 현재 칸의 오른쪽 좌표
        x2 = (c + 1) * cell_w

        # ---------------------------------------
        # 현재 칸의 이미지를 잘라낸다.
        # ---------------------------------------

        # maze_img에서 현재 칸만 추출한다.
        cell = maze_img[y1:y2, x1:x2]

        # ---------------------------------------
        # 칸의 중앙 부분만 선택한다.
        # ---------------------------------------

        # 테두리에는 검은 선이 있기 때문에
        # 전체 평균을 계산하면 오차가 발생할 수 있다.
        #
        # 따라서 가운데 50% 영역만 사용하여
        # 색상을 판별한다.
        center = cell[
            cell_h // 4 : cell_h * 3 // 4,
            cell_w // 4 : cell_w * 3 // 4
        ]

        # ---------------------------------------
        # 평균 색 계산
        # ---------------------------------------

        # OpenCV는 RGB가 아니라
        # BGR 순서를 사용한다.
        #
        # [:,:,0] → Blue
        # [:,:,1] → Green
        # [:,:,2] → Red

        b = np.mean(center[:, :, 0])
        g = np.mean(center[:, :, 1])
        r_color = np.mean(center[:, :, 2])

        # ---------------------------------------
        # 검은색이면 벽
        # ---------------------------------------

        # 파랑, 초록, 빨강 값이 모두 작으면
        # 거의 검은색이라고 판단한다.
        if b < 60 and g < 60 and r_color < 60:

            # 벽은 1로 저장
            row.append(1)

        # ---------------------------------------
        # 초록색이면 시작점
        # ---------------------------------------

        # Green 값이 크고
        # Red 값이 작으면
        # 초록색으로 판단한다.
        elif g > 150 and r_color < 120:

            # 시작점도 이동 가능한 칸이므로 0
            row.append(0)

            # 시작 좌표 저장
            start = (r, c)

        # ---------------------------------------
        # 빨간색이면 도착점
        # ---------------------------------------

        # Red 값이 크고
        # Green 값이 작으면
        # 빨간색으로 판단한다.
        elif r_color > 150 and g < 120:

            # 도착점도 이동 가능한 칸
            row.append(0)

            # 도착 좌표 저장
            goal = (r, c)

        # ---------------------------------------
        # 나머지는 길
        # ---------------------------------------

        # 흰색 또는 회색인 경우
        # 이동 가능한 길로 판단한다.
        else:

            row.append(0)

    # 한 행의 분석이 끝났으면
    # maze 리스트에 추가한다.
    maze.append(row)


# ==========================================================
# 9. 인식 결과 출력
# ==========================================================

print()

print("========== 미로 ==========")

# 생성된 미로 배열을 출력한다.
# 0은 길, 1은 벽을 의미한다.
for row in maze:
    print(row)

print()

# 시작점과 도착점의 좌표를 출력한다.
print("시작 :", start)
print("도착 :", goal)

from collections import deque

# ==========================================================
# 10. Graph 클래스 정의
# ==========================================================

# 미로를 그래프(Graph) 형태로 표현하기 위한 클래스
# 하나의 이동 가능한 칸을 하나의 정점(Vertex)으로 생각한다.

class Graph:

    # 생성자
    def __init__(self):

        # 그래프를 저장할 딕셔너리
        self.graph = {}


    # 정점 추가
    def add_vertex(self, vertex):

        # 아직 없는 정점이라면 생성
        if vertex not in self.graph:

            # 연결된 정점을 저장할 빈 리스트 생성
            self.graph[vertex] = []

    # 간선 추가
    def add_edge(self, start, end):

        # 시작 정점이 없으면 생성
        self.add_vertex(start)

        # 도착 정점이 없으면 생성
        self.add_vertex(end)

        # 시작 정점에서 도착 정점으로 이동 가능하도록 연결
        self.graph[start].append(end)

    # ------------------------------------------------------
    # BFS(너비 우선 탐색)
    # ------------------------------------------------------
    def bfs(self, start, goal):

        # 큐 생성
        queue = deque()

        # 방문한 정점 저장
        visited = set()


        # 부모 정점 저장
        parent = {}

        # ---------------------------------------------
        # 시작 정점을 큐에 삽입
        # ---------------------------------------------

        queue.append(start)

        # 시작 정점을 방문 처리
        visited.add(start)

        print("\n========== BFS 시작 ==========\n")

        # ---------------------------------------------
        # 큐가 빌 때까지 반복
        # ---------------------------------------------
        while queue:

            # 큐의 맨 앞 정점을 꺼낸다.
            #
            # FIFO 구조
            current = queue.popleft()

            print("현재 방문 :", current)

            # -----------------------------------------
            # 도착점인지 확인
            # -----------------------------------------

            if current == goal:

                print("\n도착점 발견\n")

                # -----------------------------
                # 최단 경로 복원
                # -----------------------------

                path = []

                # 부모를 따라가면서
                # 역순으로 경로를 저장한다.
                while current != start:

                    path.append(current)

                    current = parent[current]

                # 시작점 추가
                path.append(start)

                # 현재는 역순이므로
                # 시작→도착 순서로 뒤집는다.
                path.reverse()

                return path

            # -----------------------------------------
            # 현재 정점과 연결된 정점 확인
            # -----------------------------------------

            for neighbor in self.graph[current]:

                # 아직 방문하지 않은 정점이라면
                if neighbor not in visited:

                    # 방문 처리
                    visited.add(neighbor)

                    # 부모 저장
                    parent[neighbor] = current

                    # 큐에 삽입
                    queue.append(neighbor)

                    print("   →", neighbor, "큐에 추가")

            # 현재 큐 상태 출력
            print("현재 큐 :", list(queue))
            print()

        # ---------------------------------------------
        # 도착하지 못한 경우
        # ---------------------------------------------
        return []
    
# ==========================================================
# 11. 그래프(Graph) 생성
# ==========================================================

# Graph 객체 생성
# 이후 모든 정점(Vertex)과 간선(Edge)은
# g 객체 안에 저장된다.
g = Graph()

# ----------------------------------------------------------
# 상, 하, 좌, 우 이동을 위한 방향 벡터
# ----------------------------------------------------------

# dx와 dy를 이용하면
#
# 현재 위치가 (r, c)일 때
#
# 위    : (r-1, c)
# 아래  : (r+1, c)
# 왼쪽  : (r, c-1)
# 오른쪽: (r, c+1)
#
# 을 쉽게 계산할 수 있다.
dx = [-1, 1, 0, 0]
dy = [0, 0, -1, 1]


# ==========================================================
# 미로 전체를 탐색하며 그래프 생성
# ==========================================================

# 모든 행을 반복
for r in range(ROWS):

    # 모든 열을 반복
    for c in range(COLS):

        # ------------------------------------------
        # 현재 칸이 벽인지 확인
        # ------------------------------------------

        # 벽은 이동할 수 없으므로
        # 그래프에 추가하지 않는다.
        if maze[r][c] == 1:
            continue

        # 현재 위치를 하나의 정점(Vertex)로 사용한다.
        current = (r, c)

        # 그래프에 현재 정점을 추가한다.
        g.add_vertex(current)

        # ------------------------------------------
        # 현재 칸의 상, 하, 좌, 우 확인
        # ------------------------------------------

        # 네 방향을 차례대로 검사한다.
        for i in range(4):

            # 이동 후 좌표 계산
            nr = r + dx[i]
            nc = c + dy[i]

            # --------------------------------------
            # 미로 범위를 벗어나는지 검사
            # --------------------------------------

            # 범위를 벗어나면 검사하지 않는다.
            if 0 <= nr < ROWS and 0 <= nc < COLS:

                # ----------------------------------
                # 이동 가능한 칸인지 확인
                # ----------------------------------

                # 이동 가능한 길이라면
                if maze[nr][nc] == 0:

                    # 현재 칸과 이웃 칸을 연결한다.
                    g.add_edge(current, (nr, nc))

# ==========================================================
# 12. BFS 실행
# ==========================================================

# 시작점(start)부터 도착점(goal)까지
# BFS를 이용하여 최단 경로를 탐색한다.
#
# 반환값(path)은
# [(0,0), (1,0), (2,0), ...]
# 형태의 좌표 리스트이다.
path = g.bfs(start, goal)


# ==========================================================
# 13. 탐색 결과 출력
# ==========================================================

print()
print("========== BFS 결과 ==========")

# ----------------------------------------------------------
# 경로가 존재하지 않는 경우
# ----------------------------------------------------------
if len(path) == 0:

    print("경로를 찾지 못했습니다.")

# ----------------------------------------------------------
# 경로를 찾은 경우
# ----------------------------------------------------------
else:

    print("최단 경로")

    # path에 저장된 좌표를 하나씩 출력한다.
    for p in path:
        print(p)

    print()

    # 이동 횟수는
    # 방문한 정점 개수 - 1 이다.
    print("이동 횟수 :", len(path) - 1)

# ==========================================================
# 14. 결과 이미지를 생성
# ==========================================================

# 원본 미로 이미지를 복사한다.
#
# copy()를 사용하는 이유는
# 원본 이미지를 변경하지 않기 위해서이다.
result = maze_img.copy()

# ==========================================================
# 15. 최단 경로를 이미지에 표시
# ==========================================================

# path에 저장된 좌표를 하나씩 가져온다.
for r, c in path:

    # ------------------------------------------
    # 현재 칸의 가운데 좌표 계산
    # ------------------------------------------

    # 열 번호 → x좌표
    center_x = c * cell_w + cell_w // 2

    # 행 번호 → y좌표
    center_y = r * cell_h + cell_h // 2

    # ------------------------------------------
    # 파란색 원을 그린다.
    # ------------------------------------------

    # cv2.circle(
    #     이미지,
    #     중심좌표,
    #     반지름,
    #     색(BGR),
    #     두께(-1은 채우기)
    # )
    cv2.circle(
        result,
        (center_x, center_y),
        8,
        (255, 0, 0),
        -1
    )

# ==========================================================
# 16. 시작점 표시
# ==========================================================

# 시작점의 가운데 좌표 계산
sx = start[1] * cell_w + cell_w // 2
sy = start[0] * cell_h + cell_h // 2

# 초록색 원을 그린다.
cv2.circle(
    result,
    (sx, sy),
    10,
    (0, 255, 0),
    -1
)

# ==========================================================
# 17. 도착점 표시
# ==========================================================

# 도착점의 가운데 좌표 계산
gx = goal[1] * cell_w + cell_w // 2
gy = goal[0] * cell_h + cell_h // 2

# 빨간색 원을 그린다.
cv2.circle(
    result,
    (gx, gy),
    10,
    (0, 0, 255),
    -1
)

# ==========================================================
# 18. 결과 저장
# ==========================================================

# 최단 경로가 그려진 이미지를
# result.png라는 이름으로 저장한다.
cv2.imwrite("result.png", result)

print()
print("result.png 저장 완료")


# ==========================================================
# 19. 결과 화면 출력
# ==========================================================

# 결과 이미지를 화면에 출력한다.
cv2.imshow("Maze BFS", result)

