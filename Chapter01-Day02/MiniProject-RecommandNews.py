import feedparser   # Google News RSS에서 뉴스를 가져오기 위한 라이브러리

# -------------------------------------------------------
# 사용자에게 검색할 키워드를 입력
# 여러 개의 키워드는 쉼표(,)로 구분
#
# 입력 순서에 따른 우선순위 부여
# -------------------------------------------------------
keywords = input("검색할 키워드를 입력하세요(우선순위 순서, 쉼표로 구분) : ")

# -------------------------------------------------------
# 입력받은 문자열을 리스트로 변환
#
# split(",") : 쉼표를 기준으로 문자열 분리
# strip()    : 앞뒤 공백 제거
# lower()    : 대소문자 구분 없이 비교하기 위해 소문자로 변환
# -------------------------------------------------------
keyword_list = [word.strip().lower() for word in keywords.split(",")]

# -------------------------------------------------------
# 여러 개의 키워드를 OR 조건으로 연결
#
# 입력한 키워드 중 하나라도 포함된 기사를 검색한다.
# -------------------------------------------------------
search_query = "%20OR%20".join(keyword_list)

# -------------------------------------------------------
# Google News RSS 주소
#
# q          : 검색어
# when:7d    : 최근 7일 기사
# hl=ko      : 한국어
# gl=KR      : 대한민국 기준
# ceid=KR:ko : 한국 뉴스
# -------------------------------------------------------
url = (
    f"https://news.google.com/rss/search?"
    f"q={search_query}+when:7d"
    f"&hl=ko"
    f"&gl=KR"
    f"&ceid=KR:ko"
)

# RSS에서 기사 가져오기
feed = feedparser.parse(url)

# 기사 정보를 저장할 리스트
news = []

# -------------------------------------------------------
# 가져온 기사들을 하나씩 확인
# -------------------------------------------------------
for article in feed.entries:

    # 기사 제목
    title = article.title

    # 기사 링크
    link = article.link

    # 비교를 위해 소문자로 변환
    title_lower = title.lower()

    # ---------------------------------------------------
    # 첫 번째 키워드가 제목에 없으면
    # 추천 대상에서 제외한다.
    # ---------------------------------------------------
    if keyword_list[0] not in title_lower:
        continue

    # 기사 점수 초기화
    score = 0

    # 포함된 키워드 개수 초기화
    match_count = 0

    # ---------------------------------------------------
    # 기사 제목을 기준으로 점수를 계산한다.
    # ---------------------------------------------------
    for i, keyword in enumerate(keyword_list):

        # 제목에 키워드가 포함되어 있는지 확인
        if keyword in title_lower:

            match_count += 1

            # 첫 번째 키워드 (가장 높은 우선순위)
            if i == 0:
                score += 30

            # 두 번째 키워드 (두 번째 우선순위)
            elif i == 1:
                score += 20

            # 세 번째 이후 키워드 (세 번째 이후 우선순위)
            else:
                score += 10

    # ---------------------------------------------------
    # 포함된 키워드가 많을수록 관련성이 높다고 판단하여
    # 키워드 하나당 5점을 추가
    # ---------------------------------------------------
    score += match_count * 5

    # ---------------------------------------------------
    # 제목이 첫 번째 키워드로 시작하면 관련성이 높다고 판단하여 
    # 10점을 추가
    # ---------------------------------------------------
    if title_lower.startswith(keyword_list[0]):
        score += 10

    # 기사 정보 저장
    news.append({
        "title": title,
        "link": link,
        "score": score
    })

print("\n========== 추천 기사 ==========\n")
print("※ 기사 제목을 기준으로 점수를 계산합니다.")
print("※ 첫 번째 키워드는 반드시 포함되어야 합니다.")
print("※ 입력한 키워드의 우선순위를 반영합니다.\n")

rank = 1

# ======================================================
# Greedy 알고리즘
#
# 현재 남아있는 기사 중
# 점수가 가장 높은 기사를 선택
#
# 선택한 기사는 삭제한 후
# 다시 가장 높은 점수의 기사를 선택
#
# 이 과정을 반복하여
# 상위 5개의 기사를 추천
# ======================================================
while rank <= 5 and len(news) > 0:

    best = 0

    # 가장 높은 점수 찾기
    for i in range(1, len(news)):

        if news[i]["score"] > news[best]["score"]:
            best = i

    # 점수가 0점이면 종료
    if news[best]["score"] == 0:
        break

    print(f"{rank}위")
    print("제목 :", news[best]["title"])
    print("점수 :", news[best]["score"])
    print("링크 :", news[best]["link"])
    print("-" * 70)

    # 이미 추천한 기사는 삭제
    news.pop(best)

    rank += 1

# 추천 결과가 없는 경우
if rank == 1:
    print("최근 1주일 동안 조건에 맞는 기사가 없습니다.")