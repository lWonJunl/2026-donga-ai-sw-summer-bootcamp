def recommend_news(news_list, category):
    # 입력한 분야의 기사만 필터링해서 저장
    filtered_news = []
    for news in news_list:
        if news[1] == category:
            filtered_news.append(news)

    # 해당 분야의 기사가 없을 때 예외처리
    if len(filtered_news) == 0:
        print("해당 분야의 기사가 없습니다.")
        return

    # 조회수 기준 내림차순 정렬 (Greedy Algorithm 적용)
    filtered_news.sort(key=lambda x: x[2], reverse=True)

    # 기사 출력
    print("=" * 50)
    print("[추천 기사]")
    print("=" * 50)

    news = filtered_news[0]
    print(f"제목   : {news[0]}")
    print(f"분야   : {news[1]}")
    print(f"조회수 : {news[2]:,}회")
    print("-" * 50)


